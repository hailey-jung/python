#튜플, 리스트, 사전: 컨테이너 형

#Tuple
my_tuple = ("one", "two", "three", "four")
print(my_tuple[0])
print(my_tuple[3])

#List
my_list = ["one", "two", "three", "four"]
print(my_list[0])
print(my_list[3])

#리스트, 사전: 목록 추가, 변경, 삭제 필요한 프로그램에서 사용

#Dictionary
my_dictionary = {1:"one", 2:"two", 3:"three", 4:"four"}
print(my_dictionary[1])
print(my_dictionary[3])
my_dictionary = {"1":"one", "2":"two", "3":"three", "4":"four"}
print(my_dictionary["1"])
print(my_dictionary["3"])
#1,2,3,4 : 키 one,two,three,four : 값
